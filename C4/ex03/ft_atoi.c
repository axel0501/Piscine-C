int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	value;

	value = 0;
	sign = 1;
	i = 0;
	while (str[i] <= 32)
	{
		i++;
	}
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		value = (value * 10) + (str[i] - 48);
		i++;
	}
	return (value * sign);
}
