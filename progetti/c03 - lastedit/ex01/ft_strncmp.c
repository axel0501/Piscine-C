int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int diff;

	i = 0;
	while ((i < n) && (s1[i] != '\0'))
		diff += s1[i++];
	i = 0;
	while ((i < n) && (s2[i] != '\0'))
		diff -= s2[i++];
	return (diff);
}
