int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int diff;

	i = 0;
	while (s1[i] != '\0')
		diff += s1[i++];
	i = 0;
	while (s1[i] != '\0')
		diff -= s1[i++];
	return (diff);
}
