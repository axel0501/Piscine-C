int	ft_check_values(char *cv)
{
	int i;

	i = 0;
	while (cv[i] != '\0')
	{
		if (i < 31)
		{
			if (i % 2 == 0)
			{
				if (cv[i] < 49 || cv[i] > 52)
					return (0);
			}
			else
			{
				if (cv[i] != ' ')
					return (0);
			}
			i++;
		}
		else
			return (0);
	}
	if (i < 31)
		return (0);
	return (1);
}
