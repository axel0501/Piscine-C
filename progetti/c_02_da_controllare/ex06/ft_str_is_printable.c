int		ft_str_is_printable(char *str)
{
	int check;
	int i;

	check = 1;
	i = 0;
	while ((str[i] != '\0') && (check == 1))
	{
		if (str[i] < 32 || str[i] > 127)
			check = 0;
		i++;
	}
	return (check);
}
